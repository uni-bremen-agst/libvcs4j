package org.conqat.lib.commons.filesystem;

import java.io.File;
import java.io.FileFilter;

/**
 * This is a file filter which accepts only plain files and rejects directories.
 * 
 * @author Benjamin Hummel
 * @author $Author: kinnen $
 * @version $Rev: 41751 $
 * @ConQAT.Rating GREEN Hash: 540C7AB3908114199195BC38522CE5B0
 */
public class FileOnlyFilter implements FileFilter {

	/** Accepts files and rejects directories. */
	@Override
	public boolean accept(File file) {
		return file.isFile();
	}
}