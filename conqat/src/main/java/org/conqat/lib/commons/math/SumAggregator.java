package org.conqat.lib.commons.math;

/**
 * Sum aggregator.
 * 
 * @author deissenb
 * @author $Author: kinnen $
 * @version $Rev: 41751 $
 * @ConQAT.Rating GREEN Hash: 432821BA95155E07716B338E1C83921F
 */
public class SumAggregator extends PairwiseAggregatorBase {

	/** Returns zero. */
	@Override
	public double getNeutralElement() {
		return 0;
	}

	/** {@inheritDoc} */
	@Override
	protected double aggregate(double aggregate, double value) {
		return aggregate + value;
	}
}